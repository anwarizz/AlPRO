public class PerulanganLoop {
    public static void main(String[] args) {
        for (int i = 5; i <= 45; i=i+10) {
            System.out.println(i);
        }


        System.out.println("Jawaban: ");

        for (int x = 10; x <= 50; x=x+10) {
            System.out.println(x);
        }
    }
}
