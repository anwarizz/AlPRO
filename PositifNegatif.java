public class PositifNegatif {
    public static void main(String[] args) {

        int bilangan = 2;
        
        if (bilangan < 0) {
            System.out.println("bilangan " + bilangan + " adalah bilangan negatif.");
        } else {
            System.out.println("bilangan " + bilangan + " adalah bilangan positif.");
        }

    }
}
